<h1>The Problem...</h1>

<p>Health care in Sub-Saharan Africa is under threat with many Africans now expecting to live only until their late forties. This has been caused by the dual burden of the main infectious diseases <span class="definition" title="Acquired Immunodeficiency Syndrome">AIDS</span>, <span class="definition" title="Tubercolosis">TB</span> and malaria as well the increasing burden of non-communicable diseases included hypertension, diabetes and heart disease. Annual spending on health care in some countries is only 10-15 dollars per head. Recent proposals for aid to Africa include a greater investment in health care, teaching and research.</p>

<h3>Care</h3>
<p>Because of the increasing burden of disease, Africa urgently needs practical help to care for patients.</p>

<h3>Education</h3>
<p>Medical training is the most cost-effective means of developing health care. Whilst Africa has responded over the last 20 years with an almost quadrupling of medical and nursing schools there exists a great shortage of resources, including specialists, doctors, nurses, equipment and teaching materials.</p>

<h3>Research</h3>
<p>Health related research is vital to Africa's future. Research needs to be good quality, low cost, appropriate, locally determined, implementable and carried out within Africa.</p>

<br/>

<h2>The Proposal...</h2>
<h3>Aim</h3>
<p>Strengthen and support local capacity in health care over a 10-year period (2006 to 2016) at Kilimanjaro Christian Medical Centre (KCMC), a referral teaching hospital in Northern Tanzania.</p>

<h3>Strategy</h3>
<p>To work, teach, do clinical research in Africa and to develop and write teaching materials specific for Africa.</p>

